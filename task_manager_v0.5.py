def sync_with_file():
    # Функция для синхронизации списка задач с файлом (загрузка из файла или сохранение в файл)

    # Запрашиваем у пользователя действие: 1 - загрузить задачи из файла, 2 - сохранить задачи в файл
    file_action = int(input("Достать список задач из файла/Сохранить список задач на файл (1, 2): "))

    if file_action == 1:
        # Режим загрузки: открываем файл для чтения в кодировке UTF-8
        file = open("tasks.txt", 'r', encoding='utf-8')

        # Получаем общее количество строк в файле, чтобы потом итерироваться
        total_lines = len(file.readlines())
        # Возвращаем указатель в начало файла, так как readlines() сместил его в конец
        file.seek(0)

        # Проверяем: файл не пуст И текущий глобальный список задач tasks пуст (чтобы не дублировать)
        if file.read() != '' and tasks == []:
            # Снова возвращаем указатель в начало, так как file.read() сместил его
            file.seek(0)

            # Цикл по всем строкам файла (от 0 до total_lines-1)
            for _ in range(0, total_lines):
                # Читаем строку, удаляем пробелы по краям, разбиваем по разделителю ' | '
                task = file.readline().strip().split(' | ')
                # Формируем словарь задачи и добавляем в глобальный список tasks
                # Ожидаемый формат строки: id_task | task_name | checked
                tasks.append(dict(id_task=task[0], task_name=task[1], checked=task[2]))


        print("Список tasks принял данные из файла успешно! \u2705")
    else:
        # Режим сохранения: открываем файл для записи (перезапись)
        file = open("tasks.txt", 'w', encoding='utf-8')
        # Для каждой задачи из глобального списка tasks записываем строку в формате "id | имя | статус"
        for task in tasks:
            print(f'{task["id_task"]} | {task["task_name"]} | {task["checked"]}', file=file)

        print("Сохранение прошло успешно! \u2705")

    print()

